// ============================================================================
//   ПРАКТИКА №1 (дополнение). ПАТТЕРН «КОМПОНОВЩИК» (COMPOSITE)
//   Подробно закомментированная версия. Поведение полностью совпадает с main.cpp.
// ============================================================================
//
// ЧТО ЭТА ПРОГРАММА ДЕЛАЕТ
// ------------------------
// Программа собирает в памяти «дерево объектов», описывающее математическую
// формулу, затем рисует её в файл result.svg, и в самом конце открывает этот
// файл в браузере по умолчанию (через системную команду "start").
// SVG — это текстовый формат векторной графики, который понимает любой браузер.
//
//
// ЧТО ТАКОЕ ПАТТЕРН «КОМПОНОВЩИК»
// -------------------------------
// Паттерн (Composite) — это типовой приём из книги «Банды четырёх» (GoF).
// Идея: есть два сорта «штук»:
//   1. ЛИСТ (Leaf) — простая, неделимая штука. У неё внутри ничего нет.
//      В нашем случае Leaf — это класс Atom (буква или число: a, b, q, x, dx).
//   2. СОСТАВНОЙ (Composite) — контейнер, у которого внутри лежат другие
//      «штуки». В наш контейнер можно положить КАК Leaf, ТАК И другой Composite.
//      В нашей программе Composite — это Fraction (дробь), Integral (интеграл),
//      Indexed (выражение с подстрочником) и Matrix (матрица 3×3).
//
// Главная фишка паттерна: и Leaf, и Composite имеют ОДИН ОБЩИЙ ИНТЕРФЕЙС
// (в нашем коде это абстрактный класс Formula с методами measure() и draw()).
// Снаружи с ними работают одинаково — не важно, ты рисуешь букву или матрицу,
// вызов один и тот же: formula->draw(out, x, y). Контейнер не знает, что
// у него внутри — он просто говорит каждому ребёнку «нарисуй себя».
//
//
// КАРТА ФАЙЛА (что где лежит)
// ---------------------------
// 1. Заголовочные файлы              — #include <…>
// 2. struct Box                      — структура «размер формулы»
// 3. class  Formula                  — абстрактный базовый класс (общий интерфейс)
// 4. using  FormulaPtr               — короткое имя для умного указателя
// 5. class  Atom      : public Formula  — ЛИСТ (буква/число)
// 6. class  Fraction  : public Formula  — Composite: дробь
// 7. class  Integral  : public Formula  — Composite: интеграл
// 8. class  Indexed   : public Formula  — Composite: подстрочный индекс
// 9. class  Matrix    : public Formula  — Composite: матрица 3×3
// 10. void   writeSvg(...)            — функция записи дерева в SVG-файл
// 11. int    main()                   — собирает дерево и запускает запись
//
//
// КАКУЮ ФОРМУЛУ СОБИРАЕТ main()
// -----------------------------
// Это «индексированная матрица», где сам подстрочник — целая дробь:
//
//     ⎛ a   b   c ⎞
//     ⎜ a   b   c ⎟     ∫ x dx
//     ⎝ a   b   c ⎠     ──────
//                          q
//
// Дерево объектов (что в кого вложено):
//
//   Indexed                              ← корень всего выражения
//   ├── base: Matrix(3×3 из a/b/c)       ← база — большая матрица
//   └── sub:  Fraction                   ← подстрочник — целая дробь
//             ├── top:    Integral(x, dx)
//             └── bottom: Atom("q")
//
// Здесь использованы ВСЕ ЧЕТЫРЕ типа Composite (Indexed, Matrix, Fraction,
// Integral) и листы Atom. Это нужно, чтобы показать, что любая формула
// собирается из конструктора объектов.
// ============================================================================


// ---------------------------------------------------------------------------
// ПОДКЛЮЧЕНИЕ СТАНДАРТНЫХ БИБЛИОТЕК (headers)
// ---------------------------------------------------------------------------
// #include — это команда препроцессора C++: «вставь сюда содержимое такого-то
// заголовочного файла». Заголовочные файлы стандартной библиотеки пишутся
// в угловых скобках <…>. Свои — в кавычках "…". Здесь нам нужны:
#include <algorithm>   // для std::max — берёт максимум из двух чисел
#include <array>       // для std::array<T, N> — массив фиксированного размера
#include <cstdlib>     // для std::system — запуск команды операционной системы
#include <fstream>     // для std::ofstream — открытие файла на запись
#include <iostream>    // для std::cout — вывод в консоль
#include <memory>      // для std::unique_ptr и std::make_unique (умные указатели)
#include <string>      // для std::string — строка
#include <utility>     // для std::move — «перемещение» владения объектом


// ---------------------------------------------------------------------------
// СТРУКТУРА Box — «размер» формулы при отрисовке
// ---------------------------------------------------------------------------
// struct в C++ — это почти то же самое, что class, только все поля
// по умолчанию ПУБЛИЧНЫЕ (доступны снаружи). Обычно struct используют
// для «простых пачек данных без логики», а class — для объектов с поведением.
//
// Зачем нам Box? Когда формула рисуется, нам нужно знать ТРИ вещи о её размере:
//   w        — ширина в пикселях
//   h        — высота в пикселях
//   baseline — расстояние от верхнего края до «базовой линии» (там, где
//              мысленно стоит буква, без хвостиков снизу). Это нужно, чтобы
//              разные формулы можно было выровнять «по линии» — например,
//              чтобы числитель и знаменатель в дроби сидели правильно.
struct Box {
    double w;         // ширина (пиксели)
    double h;         // высота (пиксели)
    double baseline;  // от верха до базовой линии
};


// ---------------------------------------------------------------------------
// АБСТРАКТНЫЙ БАЗОВЫЙ КЛАСС Formula — ОБЩИЙ ИНТЕРФЕЙС
// ---------------------------------------------------------------------------
// Здесь начинается сам паттерн «Компоновщик». Formula — это «контракт»,
// которому подчиняются ВСЕ формулы (и Leaf-ы, и Composite-ы):
//   - у каждой формулы есть метод measure() — «сколько места я займу?»
//   - у каждой формулы есть метод draw() — «нарисуй меня в SVG-поток»
//
// Сам класс Formula объектов не создаёт — он АБСТРАКТНЫЙ. От него можно
// только НАСЛЕДОВАТЬСЯ (например: class Atom : public Formula).
//
// Ключевые слова, которые встретятся ниже:
//   virtual          — «эту функцию можно переопределить в потомке»
//   = 0              — «чисто виртуальная»: НЕТ реализации, потомок ОБЯЗАН написать
//   override         — «я переопределяю виртуальную функцию из базы» (для проверки)
//   const после ()   — «эта функция НЕ меняет состояние объекта» (она «только читает»)
class Formula {
public:
    // ВИРТУАЛЬНЫЙ ДЕСТРУКТОР. Деструктор — это специальный метод, который
    // вызывается, когда объект удаляется (выходит из памяти). Слово virtual
    // здесь критично: без него при удалении объекта через указатель на
    // базовый класс (Formula*) деструктор потомка НЕ вызовется, и память
    // вложенных объектов утечёт. = default означает «компилятор, сгенерируй
    // стандартный пустой деструктор сам».
    virtual ~Formula() = default;

    // ЧИСТО ВИРТУАЛЬНЫЕ методы (= 0). Они объявлены, но не реализованы здесь.
    // Каждый потомок ОБЯЗАН их реализовать, иначе не скомпилируется.
    // Это и есть «контракт» паттерна.

    // measure() — посчитать свой размер и вернуть Box.
    // const говорит компилятору: «этот метод ничего не меняет внутри объекта,
    // только смотрит». Это важно для безопасности и оптимизации.
    virtual Box measure() const = 0;

    // draw() — нарисовать себя в поток out, начиная с координаты (x, y).
    // std::ostream& — это «ССЫЛКА на поток вывода». Ссылка (&) — как
    // указатель, но проще: это просто другое имя для того же объекта.
    // Передаём по ссылке, чтобы не копировать поток (это вообще запрещено).
    virtual void draw(std::ostream& out, double x, double y) const = 0;
};

// ---------------------------------------------------------------------------
// АЛИАС типа: FormulaPtr — это «умный указатель на Formula»
// ---------------------------------------------------------------------------
// using A = B; — задаёт новое имя A для типа B. Дальше мы пишем FormulaPtr,
// а компилятор подставляет std::unique_ptr<Formula>.
//
// Что такое std::unique_ptr<T>?
//   Это «умный указатель» — обёртка над сырым указателем, которая САМА
//   удаляет объект, когда выходит из области видимости.
//   Слово unique значит «единоличное владение»: только ОДИН unique_ptr
//   может владеть данным объектом. Скопировать его нельзя — но можно
//   ПЕРЕДАТЬ владение через std::move (об этом ниже).
//
// Зачем нужен умный указатель?
//   - Без него мы бы писали Formula* и обязаны были бы вручную делать delete.
//     Легко забыть и получить утечку памяти.
//   - unique_ptr сам вызывает delete в нужный момент.
//   - Когда родительский объект (например, Fraction) удаляется,
//     unique_ptr на его детей тоже удаляется, и дети рекурсивно стираются.
using FormulaPtr = std::unique_ptr<Formula>;


// ===========================================================================
// КЛАСС Atom — ЛИСТ (Leaf) паттерна
// ===========================================================================
// Atom — это самая простая «штука»: одна буква или короткая строка
// (например, "a", "q", "x", "dx", "11"). У неё внутри ничего нет.
// Рисует себя как обычный SVG-тег <text>.
//
// Наследование: class Atom : public Formula — означает «Atom это
// разновидность Formula» (наследует все её методы и обязан реализовать
// чисто виртуальные).
class Atom : public Formula {
    // private поля (по умолчанию у class всё private):
    std::string text_;   // сама строка-текст. Подчёркивание в конце имени
                         // — это стилистический приём «обозначаю поле».
    double fontSize_;    // размер шрифта в пикселях

public:
    // КОНСТРУКТОР. Это специальный метод, который вызывается при создании
    // объекта. Здесь у него ДВА параметра, второй с значением по умолчанию
    // (= 22.0) — можно не указывать.
    //
    // Слово explicit запрещает «случайное» неявное преобразование.
    // Без него можно было бы случайно написать `Atom a = "x";` и
    // компилятор сам бы соорудил Atom из строки. Это иногда нежелательно.
    //
    // Что после двоеточия — СПИСОК ИНИЦИАЛИЗАЦИИ:
    //   text_(std::move(text)) — поле text_ инициализируется значением text
    //   fontSize_(fontSize)    — поле fontSize_ инициализируется значением fontSize
    // Это эффективнее, чем присваивать в теле конструктора (там было бы
    // двойное создание: сначала default, потом перезапись).
    //
    // std::move(text) — «передай владение этой строкой полю text_».
    // Без move строка бы скопировалась (это медленнее).
    explicit Atom(std::string text, double fontSize = 22.0)
        : text_(std::move(text)), fontSize_(fontSize) {}

    // measure() — посчитать размер. override говорит компилятору:
    // «проверь, что я действительно переопределяю функцию из базы».
    // Если в базе функция переименуется — компилятор сразу выдаст ошибку.
    Box measure() const override {
        // Считаем ширину «на глаз»: длина строки * размер шрифта * коэффициент 0.55.
        // 0.55 — эмпирический множитель «средняя ширина буквы в долях от высоты»
        // (зависит от шрифта; для serif-шрифта 0.5–0.6 — хорошее приближение).
        // static_cast<double>(...) — явное приведение int к double, чтобы
        // избежать целочисленного деления.
        double w = static_cast<double>(text_.size()) * fontSize_ * 0.55;

        // Высота строки больше размера шрифта: над и под буквой есть «эфир».
        double h = fontSize_ * 1.2;

        // Базовая линия — там, где мысленно стоит буква. Обычно это
        // ~95% от размера шрифта от верха.
        double baseline = fontSize_ * 0.95;

        // return {w, h, baseline}; — это «инициализация структуры списком».
        // Эквивалент: Box b; b.w = w; b.h = h; b.baseline = baseline; return b;
        return {w, h, baseline};
    }

    // draw() — вывести SVG-тег <text> в поток out.
    void draw(std::ostream& out, double x, double y) const override {
        // Получаем свой размер, чтобы знать, куда поставить baseline.
        Box b = measure();

        // SVG-координата (x, y) в нашем коде — это ВЕРХ-ЛЕВО формулы.
        // А у тега <text> в SVG атрибут y — это БАЗОВАЯ ЛИНИЯ. Поэтому
        // мы подсчитываем y + b.baseline.
        //
        // operator<< пишет в поток. "\n" — перевод строки (для красоты файла).
        // font-style="italic" — буквы будут наклонными, как принято в математике.
        out << "<text x=\"" << x << "\" y=\"" << (y + b.baseline)
            << "\" font-family=\"Cambria Math, Times, serif\" font-style=\"italic\""
            << " font-size=\"" << fontSize_ << "\">" << text_ << "</text>\n";
    }
};


// ===========================================================================
// КЛАСС Fraction — COMPOSITE для дроби «числитель / знаменатель»
// ===========================================================================
// Хранит ДВУХ детей: top (числитель) и bottom (знаменатель).
// Каждый ребёнок — это ЛЮБАЯ Formula (буква, другая дробь, матрица, что угодно).
// Это и есть суть Composite: контейнер не знает, что у него внутри.
//
// Рисует:
//   - числитель сверху, центрирован по горизонтали
//   - горизонтальная черта посередине во всю ширину
//   - знаменатель снизу, тоже центрирован
class Fraction : public Formula {
    FormulaPtr top_;     // умный указатель на числитель
    FormulaPtr bottom_;  // умный указатель на знаменатель

    // Константы класса. static — «общая на все объекты класса, не на каждый
    // отдельно». constexpr — «вычисляется на этапе компиляции» (быстрее).
    static constexpr double GAP = 4.0;   // отступ от черты до числителя/знаменателя
    static constexpr double SIDE = 8.0;  // боковые поля слева/справа

public:
    // Конструктор берёт два умных указателя — «забирает себе» владение детьми.
    // Поскольку unique_ptr нельзя копировать, передавать его можно ТОЛЬКО
    // через std::move. Поэтому в main() мы пишем std::move(integral) и т.д.
    Fraction(FormulaPtr top, FormulaPtr bottom)
        : top_(std::move(top)), bottom_(std::move(bottom)) {}

    // Размер дроби считается так:
    //   ширина  = максимум из ширины числителя и знаменателя + поля слева/справа
    //   высота  = высота числителя + два отступа + высота знаменателя
    //   baseline= ровно посередине, на уровне горизонтальной черты
    Box measure() const override {
        // top_->measure() — «вызови measure() у объекта, на который указывает top_».
        // Стрелочка -> используется для указателей, точка . — для самих объектов.
        Box t = top_->measure();
        Box b = bottom_->measure();
        double w = std::max(t.w, b.w) + 2 * SIDE;  // ширина по большему ребёнку
        double h = t.h + 2 * GAP + b.h;            // суммируем все вертикальные части
        double baseline = t.h + GAP;               // черта дроби = базовая линия
        return {w, h, baseline};
    }

    // Рисует дробь.
    void draw(std::ostream& out, double x, double y) const override {
        // Считаем свой и детский размер (нужно для центрирования).
        Box me = measure();
        Box t = top_->measure();
        Box b = bottom_->measure();

        // ЧИСЛИТЕЛЬ: вызываем draw() ребёнка, центрируя по горизонтали.
        // Формула центрирования: x + (моя_ширина - ширина_ребёнка) / 2.
        // Это РЕКУРСИЯ паттерна: родитель сам не рисует букву —
        // он перепоручает рисование ребёнку, просто сдвинув координаты.
        top_->draw(out, x + (me.w - t.w) / 2.0, y);

        // ГОРИЗОНТАЛЬНАЯ ЧЕРТА. Расположена сразу под числителем.
        double barY = y + t.h + GAP;
        // SVG-тег <line> рисует прямую от (x1, y1) до (x2, y2).
        // stroke — цвет, stroke-width — толщина в пикселях.
        out << "<line x1=\"" << x << "\" y1=\"" << barY
            << "\" x2=\"" << (x + me.w) << "\" y2=\"" << barY
            << "\" stroke=\"black\" stroke-width=\"1.5\"/>\n";

        // ЗНАМЕНАТЕЛЬ: тоже центрируем по горизонтали, ставим ниже черты.
        bottom_->draw(out, x + (me.w - b.w) / 2.0, barY + GAP);
    }
};


// ===========================================================================
// КЛАСС Integral — COMPOSITE для интеграла ∫…d?
// ===========================================================================
// Хранит ДВУХ детей: body (подынтегральное выражение) и diff (дифференциал
// типа dx). Внимание: в чужом примере "dx" был ВШИТ в код. У нас diff —
// это полноценная Formula, можно подставить хоть Atom("dy"), хоть дробь.
//
// Рисует:
//   слева — крупный символ ∫ (его высота масштабируется под высоту body)
//   правее — body
//   ещё правее — diff
class Integral : public Formula {
    FormulaPtr body_;
    FormulaPtr diff_;
    static constexpr double V_PAD = 6.0;  // вертикальный отступ сверху/снизу
    static constexpr double GAP = 6.0;    // горизонтальный зазор между частями

public:
    Integral(FormulaPtr body, FormulaPtr diff)
        : body_(std::move(body)), diff_(std::move(diff)) {}

    Box measure() const override {
        Box b = body_->measure();
        Box d = diff_->measure();

        // Общая высота интеграла = максимум высот его детей + поля.
        double h = std::max(b.h, d.h) + 2 * V_PAD;

        // Ширина символа ∫ — примерно 40% от высоты (он узкий и высокий).
        double signW = h * 0.4;

        // Ширина всего интеграла = символ ∫ + промежуток + body + промежуток + diff.
        double w = signW + GAP + b.w + GAP + d.w;

        // Базовая линия — посередине по высоте. Этого достаточно
        // для нашей задачи (упрощение).
        return {w, h, h / 2.0};
    }

    void draw(std::ostream& out, double x, double y) const override {
        Box me = measure();
        Box b = body_->measure();
        Box d = diff_->measure();

        // Считаем размеры символа ∫ — растёт вместе с body.
        double signW = me.h * 0.4;
        double signFont = me.h * 0.95;

        // СИМВОЛ ∫. В SVG/HTML символ можно записать ЧИСЛОВОЙ ССЫЛКОЙ:
        // &#8747; — это юникод U+222F (∫). Так удобнее, чем писать
        // сам символ в исходнике (не зависит от кодировки файла).
        // y + me.h * 0.85 — это где будет базовая линия символа.
        out << "<text x=\"" << x << "\" y=\"" << (y + me.h * 0.85)
            << "\" font-family=\"Cambria Math, Times, serif\""
            << " font-size=\"" << signFont
            << "\" font-weight=\"100\">&#8747;</text>\n";

        // ПОДЫНТЕГРАЛЬНОЕ ВЫРАЖЕНИЕ. Центрируем по вертикали (по середине
        // интеграла). РЕКУРСИЯ: говорим body «нарисуй себя в этом месте».
        double bodyX = x + signW + GAP;
        double bodyY = y + (me.h - b.h) / 2.0;
        body_->draw(out, bodyX, bodyY);

        // ДИФФЕРЕНЦИАЛ (dx и т.п.). Тоже центрируем по вертикали.
        double diffX = bodyX + b.w + GAP;
        double diffY = y + (me.h - d.h) / 2.0;
        diff_->draw(out, diffX, diffY);
    }
};


// ===========================================================================
// КЛАСС Indexed — COMPOSITE для выражения с подстрочником (aᵢ, Mₙ, ...)
// ===========================================================================
// Хранит ДВУХ детей: base (основа) и sub (подстрочник).
//
// КЛЮЧЕВОЕ ОТЛИЧИЕ ОТ ЧУЖОГО ПРИМЕРА: там подстрочник был встроен В САМ ЛИСТ
// как строка subText. У нас sub — это ПОЛНОЦЕННАЯ Formula. Значит, в качестве
// подстрочника можно подставить хоть дробь, хоть матрицу, хоть интеграл.
// В нашей формуле sub — это целая дробь с интегралом сверху!
//
// Рисует:
//   слева — base нормального размера
//   справа-снизу — sub, уменьшенный через SVG transform="scale(SUB_SCALE)"
class Indexed : public Formula {
    FormulaPtr base_;
    FormulaPtr sub_;

    // Насколько подстрочник МЕНЬШЕ основы (0.65 = 65% от размера).
    static constexpr double SUB_SCALE = 0.65;
    // Насколько ниже базовой линии «опускается» подстрочник.
    // 0.45 значит «опустить вниз почти на половину высоты базы».
    static constexpr double DROP_FRAC = 0.45;

public:
    Indexed(FormulaPtr base, FormulaPtr sub)
        : base_(std::move(base)), sub_(std::move(sub)) {}

    Box measure() const override {
        Box b = base_->measure();
        Box s = sub_->measure();

        // Размеры подстрочника после уменьшения.
        double sw = s.w * SUB_SCALE;
        double sh = s.h * SUB_SCALE;

        // Где сверху начинается подстрочник.
        double subTop = b.h * (1.0 - DROP_FRAC);

        // Итоговая ширина = ширина основы + ширина уменьшенного подстрочника.
        double w = b.w + sw;
        // Итоговая высота = максимум из (высота основы) и (низ подстрочника).
        double h = std::max(b.h, subTop + sh);

        return {w, h, b.baseline};
    }

    void draw(std::ostream& out, double x, double y) const override {
        Box b = base_->measure();

        // РЕКУРСИЯ: рисуем базу в исходных координатах.
        base_->draw(out, x, y);

        // Координаты, куда «приклеить» подстрочник.
        double subTop = b.h * (1.0 - DROP_FRAC);
        double sx = x + b.w;
        double sy = y + subTop;

        // ТРЮК: вместо того, чтобы пересчитывать каждый <text> с уменьшенным
        // font-size, мы оборачиваем всё в SVG-группу <g> с трансформацией
        // translate (сдвиг) + scale (масштаб). Браузер сам уменьшит всё внутри.
        // Поэтому ниже sub_->draw(out, 0, 0) — рисуем как будто из начала координат,
        // а реальная позиция и масштаб уже заданы в transform.
        out << "<g transform=\"translate(" << sx << "," << sy
            << ") scale(" << SUB_SCALE << ")\">\n";
        sub_->draw(out, 0, 0);
        out << "</g>\n";
    }
};


// ===========================================================================
// КЛАСС Matrix — COMPOSITE для матрицы 3×3
// ===========================================================================
// Хранит ДЕВЯТЬ детей в виде двумерного массива cells_[r][c].
// Метод setCell(r, c, …) — это и есть тот самый «метод добавления фигуры
// в фигуру-контейнер» из условия задачи.
//
// Рисует:
//   слева — открывающая круглая скобка (рисуется кривой Безье через <path>)
//   справа — закрывающая
//   внутри — 9 ячеек: для каждой ячейки в нужном месте вызываем её draw().
//
// Самое важное: высота строк и ширина столбцов считаются АВТОМАТИЧЕСКИ
// по фактическим размерам детей. Это и есть требование «контейнер
// подстраивается под размер вложенного элемента».
class Matrix : public Formula {
    // std::array<T, N> — массив фиксированного размера N элементов типа T.
    // У нас «массив из 3 массивов по 3 умных указателя» = матрица 3×3.
    std::array<std::array<FormulaPtr, 3>, 3> cells_;

    static constexpr double CELL_H_GAP = 16.0;  // зазор между столбцами
    static constexpr double CELL_V_GAP = 10.0;  // зазор между строками
    static constexpr double BRACKET_W = 12.0;   // ширина круглой скобки
    static constexpr double INNER_PAD = 6.0;    // отступ от скобки до контента

    // Вспомогательный приватный метод: пройти по всем ячейкам и посчитать
    // ШИРИНУ каждого столбца (как максимум по ширине его 3 ячеек) и
    // ВЫСОТУ каждой строки (как максимум по высоте её 3 ячеек).
    // Параметры передаются ПО ССЫЛКЕ — мы их заполняем, как «выходные».
    void grid(std::array<double, 3>& colW, std::array<double, 3>& rowH) const {
        colW.fill(0);  // обнуляем все 3 элемента
        rowH.fill(0);
        // Цикл по строкам:
        for (int r = 0; r < 3; ++r) {
            // Цикл по столбцам внутри строки:
            for (int c = 0; c < 3; ++c) {
                Box b = cells_[r][c]->measure();
                // std::max возвращает большее из двух — обновляем максимум.
                colW[c] = std::max(colW[c], b.w);
                rowH[r] = std::max(rowH[r], b.h);
            }
        }
    }

public:
    // Метод для «добавления фигуры в контейнер» — кладёт ребёнка в ячейку.
    void setCell(int r, int c, FormulaPtr child) {
        cells_[r][c] = std::move(child);
    }

    Box measure() const override {
        // {} — это инициализация массива «нулями».
        std::array<double, 3> colW{}, rowH{};
        grid(colW, rowH);  // заполняем ширины/высоты

        // Считаем общую ширину:
        // две скобки + два внутренних отступа + сумма ширин столбцов + промежутки
        double w = 2 * BRACKET_W + 2 * INNER_PAD;
        for (int c = 0; c < 3; ++c) {
            w += colW[c];
            if (c < 2) w += CELL_H_GAP;  // зазор не нужен после последнего столбца
        }

        // Общая высота: два внутренних отступа + сумма высот строк + промежутки
        double h = 2 * INNER_PAD;
        for (int r = 0; r < 3; ++r) {
            h += rowH[r];
            if (r < 2) h += CELL_V_GAP;
        }

        // Базовая линия матрицы — посередине (она «выровнена по центру»).
        return {w, h, h / 2.0};
    }

    void draw(std::ostream& out, double x, double y) const override {
        std::array<double, 3> colW{}, rowH{};
        grid(colW, rowH);
        Box me = measure();

        // ЛЕВАЯ СКОБКА. Рисуется тегом <path> с описанием d="…".
        //   M x y       — Move (поставить «перо» в точку x, y)
        //   Q cx cy x y — Quadratic Bézier: кривая до (x, y) с контрольной точкой (cx, cy)
        // Контрольная точка задаёт «изгиб». Тут она сильно левее — отсюда округлость.
        out << "<path d=\"M " << (x + BRACKET_W) << " " << y
            << " Q " << x << " " << (y + me.h / 2.0)
            << " " << (x + BRACKET_W) << " " << (y + me.h)
            << "\" fill=\"none\" stroke=\"black\" stroke-width=\"1.5\"/>\n";

        // ПРАВАЯ СКОБКА. Симметрично: контрольная точка правее — изгиб в другую сторону.
        double rx = x + me.w;
        out << "<path d=\"M " << (rx - BRACKET_W) << " " << y
            << " Q " << rx << " " << (y + me.h / 2.0)
            << " " << (rx - BRACKET_W) << " " << (y + me.h)
            << "\" fill=\"none\" stroke=\"black\" stroke-width=\"1.5\"/>\n";

        // 9 ЯЧЕЕК. Идём по строкам и столбцам, для каждой ячейки:
        //   - вычисляем верхний-левый угол ячейки (curX, curY)
        //   - центрируем содержимое внутри ячейки
        //   - вызываем draw() ребёнка (РЕКУРСИЯ — Composite внутри Composite!)
        double curY = y + INNER_PAD;
        for (int r = 0; r < 3; ++r) {
            double curX = x + BRACKET_W + INNER_PAD;
            for (int c = 0; c < 3; ++c) {
                Box cb = cells_[r][c]->measure();
                // Центрирование ячейки по горизонтали и вертикали:
                double cx = curX + (colW[c] - cb.w) / 2.0;
                double cy = curY + (rowH[r] - cb.h) / 2.0;
                cells_[r][c]->draw(out, cx, cy);
                curX += colW[c] + CELL_H_GAP;  // переходим к следующему столбцу
            }
            curY += rowH[r] + CELL_V_GAP;  // переходим к следующей строке
        }
    }
};


// ---------------------------------------------------------------------------
// ФУНКЦИЯ writeSvg — записать дерево формул в SVG-файл
// ---------------------------------------------------------------------------
// Это «обёртка», которая:
//   1) спрашивает у корневой формулы её размер,
//   2) открывает файл, пишет заголовок SVG нужных размеров,
//   3) рисует белый фон,
//   4) вызывает root.draw(...) — и тут начинается рекурсия по всему дереву,
//   5) закрывает </svg>.
//
// const Formula& root — ссылка (&) на ПОСТОЯННЫЙ (const) объект Formula.
//   - ссылка чтобы не копировать;
//   - const чтобы случайно не изменить;
//   - можем передать ЛЮБОЙ потомок (Atom, Fraction, …) — это полиморфизм.
void writeSvg(const Formula& root, const std::string& path, double margin = 30.0) {
    Box b = root.measure();
    // Размер картинки = размер формулы + поля со всех сторон.
    double W = b.w + 2 * margin;
    double H = b.h + 2 * margin;

    // Открываем файл для записи. ofstream сам закроет файл, когда выйдет из
    // области видимости (в конце функции) — благодаря деструктору.
    std::ofstream f(path);

    // Декларация XML и корневой тег <svg>:
    //   xmlns="..." — пространство имён SVG (обязательно)
    //   viewBox="0 0 W H" — внутренние координаты документа
    f << "<?xml version=\"1.0\" encoding=\"UTF-8\"?>\n";
    f << "<svg xmlns=\"http://www.w3.org/2000/svg\" width=\"" << W
      << "\" height=\"" << H << "\" viewBox=\"0 0 " << W << " " << H << "\">\n";

    // Белый фон во всё «полотно».
    f << "<rect width=\"100%\" height=\"100%\" fill=\"white\"/>\n";

    // Запускаем рекурсивную отрисовку. Координата (margin, margin) — отступ от края.
    root.draw(f, margin, margin);

    f << "</svg>\n";
    // Здесь f автоматически закрывается, файл сохранён.
}


// ===========================================================================
// ФУНКЦИЯ main — точка входа в программу
// ===========================================================================
// 1) Собираем дерево объектов «снизу вверх» (сначала листья и мелкие
//    компоненты, потом контейнеры, которые в них завёрнуты).
// 2) Передаём корень в writeSvg, чтобы записать SVG-файл.
// 3) Открываем файл командой start (это команда Windows).
int main() {
    // ----- ШАГ 1. СОЗДАЁМ МАТРИЦУ 3×3 ИЗ БУКВ a/b/c -----
    // std::make_unique<T>(args) — создаёт объект T в куче (heap) и оборачивает
    // его в unique_ptr. Это безопаснее, чем new T(args).
    auto matrix = std::make_unique<Matrix>();

    // Массив из 3 строк (буквы для трёх столбцов).
    const char* letters[3] = {"a", "b", "c"};

    // Заполняем все 9 ячеек: в столбце 0 — буква 'a', в столбце 1 — 'b', в 2 — 'c'.
    for (int r = 0; r < 3; ++r) {
        for (int c = 0; c < 3; ++c) {
            matrix->setCell(r, c, std::make_unique<Atom>(letters[c]));
        }
    }

    // ----- ШАГ 2. СОЗДАЁМ ИНТЕГРАЛ ∫ x dx -----
    // Тело — буква x, дифференциал — строка dx.
    auto integral = std::make_unique<Integral>(
        std::make_unique<Atom>("x"),
        std::make_unique<Atom>("dx")
    );

    // ----- ШАГ 3. СОЗДАЁМ ДРОБЬ (интеграл / q) — это будет подстрочником -----
    // ВНИМАНИЕ: integral после std::move «опустошается» — владение переходит к Fraction.
    // Так работает уникальный указатель: его передают, не копируют.
    auto subscript = std::make_unique<Fraction>(
        std::move(integral),
        std::make_unique<Atom>("q")
    );

    // ----- ШАГ 4. ОБОРАЧИВАЕМ ВСЁ В Indexed: матрица_с_подстрочником -----
    // База — матрица. Подстрочник — наша дробь с интегралом.
    auto root = std::make_unique<Indexed>(
        std::move(matrix),
        std::move(subscript)
    );

    // ----- ШАГ 5. ПИШЕМ SVG-ФАЙЛ -----
    const std::string path = "result.svg";
    writeSvg(*root, path);  // *root — «разыменовать указатель», получить сам объект
    std::cout << "SVG saved to " << path << "\n";

    // ----- ШАГ 6. ОТКРЫВАЕМ ФАЙЛ В БРАУЗЕРЕ -----
    // std::system запускает команду через системную оболочку (cmd на Windows).
    // start "" result.svg — встроенная команда cmd: открыть файл «по умолчанию»
    // (для .svg это обычно браузер). Пустые "" — это «заголовок окна»,
    // нужны, потому что иначе start воспримет первую кавычку как заголовок.
    std::system("start \"\" result.svg");

    // return 0; — программа завершается «успешно».
    // Когда main заканчивается, все unique_ptr (root → matrix/subscript →
    // integral/Atom-ы …) уничтожаются в обратном порядке, и память освобождается
    // полностью автоматически. Утечек нет.
    return 0;
}
